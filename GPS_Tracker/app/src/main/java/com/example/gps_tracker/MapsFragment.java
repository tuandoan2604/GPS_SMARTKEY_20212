public class MapsFragment extends Fragment {
    public GoogleMap mMap;
    private final DatabaseReference databaseReference = FirebaseDatabase.getInstance().getReference();
    ArrayList<Marker> ListMark = new ArrayList<>();
    Context activity = getContext();
    private static final int REQUEST_CODE_GPS_PERMISSION = 10;
    private OnMapReadyCallback callback = new OnMapReadyCallback() {
       @RequiresApi(api = Build.VERSION_CODES.M)
       @Override
        public void onMapReady(GoogleMap googleMap) {
           Log.d("CREATE","start run");
           mMap = googleMap;
           ((MainActivity)getActivity()).setMyLocation(mMap);
           ((MainActivity)getActivity()).getLocation(databaseReference,((MainActivity)getActivity()).deviceID,mMap);
           ((MainActivity)getActivity()).mapSelect(mMap);
        }
    };

    @Nullable
    @Override
    public View onCreateView(@NonNull LayoutInflater inflater,
                             @Nullable ViewGroup container,
                             @Nullable Bundle savedInstanceState) {
        Log.d("CREATE","create view");
        return inflater.inflate(R.layout.fragment_maps, container, false);
    }

    @Override
    public void onViewCreated(@NonNull View view, @Nullable Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);
        SupportMapFragment mapFragment =
                (SupportMapFragment) getChildFragmentManager().findFragmentById(R.id.map);
        if (mapFragment != null) {
            mapFragment.getMapAsync(callback);
        }
        Log.d("CREATE","created view");
    }

}